    private void BtnConsultarNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnConsultarNomeActionPerformed
        //Botão pesquisar
        String nome = "%" + CxConsulta.getText() + "%";
        //criar a lista de fornecedor
        FornecedorDao dao = new FornecedorDao();
        List<Fornecedor> lista = dao.buscarFornecedorPorNome(nome);
        //Uso default table model para pegar o padrão da tabela
        DefaultTableModel dados = (DefaultTableModel)TabelaFornecedores.getModel();
        //limpar os dados
        dados.setNumRows(0);
        //colocar dados na lista - para cada item uma linha - definindo ordem
        for(Fornecedor coluna : lista){
            dados.addRow(new Object[]{
                coluna.getId(),
                coluna.getNome(),
                coluna.getCnpj(),
                coluna.getEmail(),
                coluna.getTelefone(),
                coluna.getCelular(),
                coluna.getCep(),
                coluna.getEndereco(),
                coluna.getNumero(),
                coluna.getComplemento(),
                coluna.getBairro(),
                coluna.getCidade(),
                coluna.getEstado()             
            });
        }
        
    }//GEN-LAST:event_BtnConsultarNomeActionPerformed

