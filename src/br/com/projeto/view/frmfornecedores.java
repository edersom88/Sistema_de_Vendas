    private void BtnPesquisarCnpjActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPesquisarCnpjActionPerformed
        // Botão de pesquisa por CNPJ
            String cnpj = CxCnpj.getText();
            Fornecedor obj = new Fornecedor();
            FornecedorDao dao = new FornecedorDao();
            
            obj = dao.consultarPorCnpj(cnpj);
            
            //Exibição de dados nos campos
            if (obj.getCnpj() != null) {
            CxCodigo.setText(String.valueOf(obj.getId()));
            CxNome.setText(obj.getNome());
            CxCnpj.setText(obj.getCnpj());
            CxEmail.setText(obj.getEmail());
            CxTelefone.setText(obj.getTelefone());
            CxCelular.setText(obj.getCelular());
            CxCep.setText(obj.getCep());
            CxEndereco.setText(obj.getEndereco());
            CxNumero.setText(String.valueOf (obj.getNumero()));
            CxComplemento.setText(obj.getComplemento());
            CxBairro.setText(obj.getBairro());
            CxCidade.setText(obj.getCidade());
            BoxEstado.setSelectedItem(obj.getEstado());
            }
            if (obj.getCnpj() == null) {
                JOptionPane.showMessageDialog(null, "Fornecedor não localizado!!!");
            }
    }//GEN-LAST:event_BtnPesquisarCnpjActionPerformed

