    public void listar(){
        //criar a lista de clientes
       FornecedorDao dao = new FornecedorDao();
        List<Fornecedor> lista = dao.listarFornecedores();
        //Uso default table model para pegar o padrão da tabela
        DefaultTableModel dados = (DefaultTableModel)TabelaFornecedores.getModel();
        //limpar os dados
        dados.setNumRows(0);
        //colocar dados na lista - para cada item uma linha - definindo ordem
        for(Fornecedor coluna : lista){
            dados.addRow(new Object[]{
                coluna.getId(),
                coluna.getNome(),
                coluna.getCnpj(),
                coluna.getEmail(),
                coluna.getTelefone(),
                coluna.getCelular(),
                coluna.getCep(),
                coluna.getEndereco(),
                coluna.getNumero(),
                coluna.getComplemento(),
                coluna.getBairro(),
                coluna.getCidade(),
                coluna.getEstado()             
            });
        }
    }

