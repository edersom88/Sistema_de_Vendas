    private void BtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnExcluirActionPerformed
        //Botão excluir
            //vai pegar o nome que está nas caixas
            Produto obj = new Produto();
            obj.setId(Integer.parseInt(CxCodigo.getText()));
            
            ProdutosDao dao = new ProdutosDao();
            
            dao.excluir(obj);
            
            new Uteis().LimparTela(PainelEdicao);
    }//GEN-LAST:event_BtnExcluirActionPerformed

    private void BtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSalvarActionPerformed
        // botão salvar
            //vai pegar o nome que está nas caixas
            Produto obj = new Produto();
            obj.setDescricao(cxDescricao.getText());
            obj.setPreco(Double.parseDouble(cxPreco.getText()));
            obj.setQtd_estoque(Integer.parseInt(cxEstoque.getText()));
            
            //criação de objeto de fornecedor
            Fornecedor f = new Fornecedor();
            f = (Fornecedor)boxFornecedor.getSelectedItem();
            obj.setFornecedor(f);
                        
            ProdutosDao dao = new ProdutosDao();
            
            dao.cadastrar(obj);
            
            new Uteis().LimparTela(PainelEdicao);
    }//GEN-LAST:event_BtnSalvarActionPerformed

