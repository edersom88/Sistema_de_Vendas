    private void boxFornecedorAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_boxFornecedorAncestorAdded
        // Listar os fornecedores no combo box
        FornecedorDao dao = new FornecedorDao();
        //gerar lista de fornecedores
        List<Fornecedor> listadefornecedores = dao.listarFornecedores();
        //limpa o combo box toda vez que é aberto
        boxFornecedor.removeAll();
        //Gera os dados e coloca no combo box
        for(Fornecedor f : listadefornecedores){
                boxFornecedor.addItem(f);
        }
        
        
        
    }//GEN-LAST:event_boxFornecedorAncestorAdded

