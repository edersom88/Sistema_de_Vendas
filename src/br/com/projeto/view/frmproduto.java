    private void TabelaProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelaProdutosMouseClicked
        //Pegar os dados
        //Faz voltar na tela de cadastro
        PainelDeAbas.setSelectedIndex(0);
        //Passar os dados da tabela para a tela de cadastro
        CxCodigo.setText(TabelaProdutos.getValueAt(TabelaProdutos.getSelectedRow(), 0).toString());
        cxDescricao.setText(TabelaProdutos.getValueAt(TabelaProdutos.getSelectedRow(), 1).toString());
        cxPreco.setText(TabelaProdutos.getValueAt(TabelaProdutos.getSelectedRow(),2).toString());
        cxEstoque.setText(TabelaProdutos.getValueAt(TabelaProdutos.getSelectedRow(), 3).toString());
        
        Fornecedor f = new Fornecedor();
        FornecedorDao dao = new FornecedorDao();
        
        f = dao.consultarPorNome(TabelaProdutos.getValueAt(TabelaProdutos.getSelectedRow(), 4).toString());
        
        boxFornecedor.removeAllItems();
        boxFornecedor.setSelectedItem(f);
        
    }//GEN-LAST:event_TabelaProdutosMouseClicked

    private void CxConsultaProdutoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_CxConsultaProdutoKeyPressed
       //consulta apenas digitando
       String nome = "%" + CxConsultaProduto.getText() + "%";
        //criar a lista de clientes
        //criar a lista de clientes
        ProdutosDao dao = new ProdutosDao();
        List<Produto> lista = dao.listarProdutosPorNome(nome);
        //Uso default table model para pegar o padrão da tabela
        DefaultTableModel dados = (DefaultTableModel)TabelaProdutos.getModel();
        //limpar os dados
        dados.setNumRows(0);
        //colocar dados na lista - para cada item uma linha - definindo ordem
        for(Produto coluna : lista){
            dados.addRow(new Object[]{
                coluna.getId(),
                coluna.getDescricao(),
                coluna.getPreco(),
                coluna.getQtd_estoque(),
                coluna.getFornecedor().getNome()             
            });
        }
    }//GEN-LAST:event_CxConsultaProdutoKeyPressed

    private void BtnPesquisarNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPesquisarNomeActionPerformed
        // Botão de pesquisa por CPF
            String nome = cxDescricao.getText();
            Produto obj = new Produto();
            ProdutosDao dao = new ProdutosDao();
            
            obj = dao.consultarProdutosPorNome(nome);
            
            boxFornecedor.removeAllItems();
            
            //Exibição de dados nos campos
            if (obj.getDescricao() != null) {
            CxCodigo.setText(String.valueOf(obj.getId()));
            cxDescricao.setText(obj.getDescricao());
            cxPreco.setText(String.valueOf(obj.getPreco()));
            cxEstoque.setText(String.valueOf(obj.getQtd_estoque()));
            
            Fornecedor f = new Fornecedor();
            FornecedorDao fdao = new FornecedorDao();
            
            f = fdao.consultarPorNome(obj.getFornecedor().getNome());
            
            boxFornecedor.getModel().setSelectedItem(f);
            }
            if (obj.getDescricao()== null) {
                JOptionPane.showMessageDialog(null, "Produto não localizado!!!");
            }
    }//GEN-LAST:event_BtnPesquisarNomeActionPerformed

