    private void BtnConsultarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnConsultarProdutoActionPerformed
        //Botão pesquisar
        String nome = "%" + CxConsultaProduto.getText() + "%";
        //criar a lista de clientes
        ProdutosDao dao = new ProdutosDao();
        List<Produto> lista = dao.listarProdutosPorNome(nome);
        //Uso default table model para pegar o padrão da tabela
        DefaultTableModel dados = (DefaultTableModel)TabelaProdutos.getModel();
        //limpar os dados
        dados.setNumRows(0);
        //colocar dados na lista - para cada item uma linha - definindo ordem
        for(Produto coluna : lista){
            dados.addRow(new Object[]{
                coluna.getId(),
                coluna.getDescricao(),
                coluna.getPreco(),
                coluna.getQtd_estoque(),
                coluna.getFornecedor().getNome()             
            });
        }
        
    }//GEN-LAST:event_BtnConsultarProdutoActionPerformed

