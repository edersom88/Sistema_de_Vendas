    private void BtnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPesquisarActionPerformed
        // Bot√£o de pesquisa por Data
        try {
            //receber as datas da tela no formato inserido e converte para o BD
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate data_venda = LocalDate.parse(cxData.getText(), formato);

            double total_venda;
            
            VendasDao dao = new VendasDao();
            total_venda = dao.retornaValorVendido(data_venda);
            
            cxTotalVenda.setText(String.valueOf(total_venda));

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null,"Informe outra data.");
        }

    }//GEN-LAST:event_BtnPesquisarActionPerformed

