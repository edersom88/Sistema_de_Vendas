    private void BtnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAdicionarActionPerformed
        try {
            int estoque, qtdeatual;
            estoque = Integer.parseInt(cxEstoqueAtual.getText());
            
            qtdeatual = Integer.parseInt(cxQtde.getText());
                 
            qtdenova = estoque + qtdeatual;
            
            ProdutosDao dao = new ProdutosDao();
            dao.adicionarEstoque(idproduto, qtdenova);
            
            JOptionPane.showMessageDialog(null, "Estoque atualizado");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro na atualização: "+ e);
        }
    }//GEN-LAST:event_BtnAdicionarActionPerformed

