    public void listar(){
        //criar a lista de Produtos
        ProdutosDao dao = new ProdutosDao();
        List<Produto> lista = dao.listarProdutos();
        //Uso default table model para pegar o padrão da tabela
        DefaultTableModel dados = (DefaultTableModel)tabelaControleEstoque.getModel();
        //limpar os dados
        dados.setNumRows(0);
        //colocar dados na lista - para cada item uma linha - definindo ordem
        for(Produto coluna : lista){
            dados.addRow(new Object[]{
                coluna.getId(),
                coluna.getDescricao(),
                coluna.getPreco(),
                coluna.getQtd_estoque(),
                coluna.getFornecedor().getNome()
            });
        }
    }
    
