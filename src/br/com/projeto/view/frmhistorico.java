    private void BtnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPesquisarActionPerformed
        // Botão de pesquisa por Data
        try {
            //receber as datas da tela no formato inserido e converte para o BD
        DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        LocalDate data_inicio = LocalDate.parse(cxDataInicial.getText(), formato);
        LocalDate data_fim = LocalDate.parse(cxDataFinal.getText(), formato);
        
        VendasDao dao = new VendasDao();
        List<Vendas>lista = dao.listarVendasPorDatas(data_inicio, data_fim);
        
        DefaultTableModel dados = (DefaultTableModel)tabelaHistorico.getModel();
        dados.setNumRows(0);
        
        for(Vendas vd : lista){
            dados.addRow(new Object[]{
            
            vd.getId(),
            vd.getData_venda(),
            vd.getCliente().getNome(),
            vd.getTotal_venda(),
            vd.getObs()
        });
        }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null,"Não localizadas vendas.");
        }
        
    }//GEN-LAST:event_BtnPesquisarActionPerformed

