    private void tabelaHistoricoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaHistoricoMouseClicked
        // Clicar em uma venda
        frmdetalhesvenda tela = new frmdetalhesvenda();
        
        tela.cxCliente.setText(tabelaHistorico.getValueAt(tabelaHistorico.getSelectedRow(),2).toString());
        tela.cxTotalVenda.setText(tabelaHistorico.getValueAt(tabelaHistorico.getSelectedRow(),3).toString());
        tela.cxData.setText(tabelaHistorico.getValueAt(tabelaHistorico.getSelectedRow(),1).toString());
        tela.cxObs.setText(tabelaHistorico.getValueAt(tabelaHistorico.getSelectedRow(),4).toString());
        
        int venda_id = Integer.parseInt(tabelaHistorico.getValueAt(tabelaHistorico.getSelectedRow(),0).toString());
        
        //Dados dos itens da venda
        ItensVenda item = new ItensVenda();
        ItemVendaDao dao_item = new ItemVendaDao();
        List<ItensVenda> listaitens = dao_item.listarItensPorVenda(venda_id);
        
        //Uso default table model para pegar o padrão da tabela
        DefaultTableModel dados = (DefaultTableModel)tela.tabelaDetalhes.getModel();
        //limpar os dados
        dados.setNumRows(0);
        //colocar dados na lista - para cada item uma linha - definindo ordem
        for(ItensVenda coluna : listaitens){
            dados.addRow(new Object[]{
                
                coluna.getProduto().getDescricao(),
                coluna.getQtd(),
                coluna.getProduto().getPreco(),
                coluna.getSubtotal()             
            });
        }
        
        
        tela.setVisible(true);
    }//GEN-LAST:event_tabelaHistoricoMouseClicked

