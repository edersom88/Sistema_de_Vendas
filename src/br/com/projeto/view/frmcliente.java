    public void listar(){
        //criar a lista de clientes
        ClienteDao dao = new ClienteDao();
        List<Cliente> lista = dao.listarClientes();
        //Uso default table model para pegar o padrão da tabela
        DefaultTableModel dados = (DefaultTableModel)TabelaClientes.getModel();
        //limpar os dados
        dados.setNumRows(0);
        //colocar dados na lista - para cada item uma linha - definindo ordem
        for(Cliente coluna : lista){
            dados.addRow(new Object[]{
                coluna.getId(),
                coluna.getNome(),
                coluna.getRg(),
                coluna.getCpf(),
                coluna.getEmail(),
                coluna.getTelefone(),
                coluna.getCelular(),
                coluna.getCep(),
                coluna.getEndereco(),
                coluna.getNumero(),
                coluna.getComplemento(),
                coluna.getBairro(),
                coluna.getCidade(),
                coluna.getEstado()             
            });
        }
    }

