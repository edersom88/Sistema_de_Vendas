    private void BtnPesquisarCpfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPesquisarCpfActionPerformed
        // Botão de pesquisa por CPF
            String cpf = CxCpf.getText();
            Cliente obj = new Cliente();
            ClienteDao dao = new ClienteDao();
            
            obj = dao.consultarPorCpf(cpf);
            
            //Exibição de dados nos campos
            if (obj.getCpf() != null) {
            CxCodigo.setText(String.valueOf(obj.getId()));
            CxNome.setText(obj.getNome());
            CxRg.setText(obj.getRg());
            CxCpf.setText(obj.getCpf());
            CxEmail.setText(obj.getEmail());
            CxTelefone.setText(obj.getTelefone());
            CxCelular.setText(obj.getCelular());
            CxCep.setText(obj.getCep());
            CxEndereco.setText(obj.getEndereco());
            CxNumero.setText(String.valueOf (obj.getNumero()));
            CxComplemento.setText(obj.getComplemento());
            CxBairro.setText(obj.getBairro());
            CxCidade.setText(obj.getCidade());
            BoxEstado.setSelectedItem(obj.getEstado());
            }
            if (obj.getCpf() == null) {
                JOptionPane.showMessageDialog(null, "Cliente não localizado!!!");
            }
    }//GEN-LAST:event_BtnPesquisarCpfActionPerformed

